    function get_data($id) {
        global $query_em, $result, $rows, $my_data, $hm, $db, $un, $pw, $conn;
        $hm = 'localhost';
        $db = 'portfolio';
        $un = 'root';
        $pw = '';
        $conn = new mysqli($hm, $un, $pw, $db);
        $query_em = "SELECT contact FROM contacts, contact_type WHERE contacts.contact_type_id = contact_type.contact_type_id AND contact_type.contact_type_id = $id  ORDER BY contact";
        mysqli_set_charset($conn, "utf8");
        $result = $conn->query($query_em);
        mysqli_set_charset($conn, "utf8");
        $rows = mysqli_num_rows($result);
        $my_data = [];
        for ($i = 0 ; $i < $rows ; ++$i)
        {
            $row = mysqli_fetch_row($result);
                for ($j = 0 ; $j < count($row); ++$j) {
                    $my_data[] = $row[$j];
                }
        }
        $conn->close();
        return $my_data;
    }
